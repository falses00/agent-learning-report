import { existsSync, readFileSync, readdirSync, writeFileSync } from 'node:fs';
import { dirname, relative, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const dataDir = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(dataDir, '..');
const checkOnly = process.argv.includes('--check');
const testDir = resolve(repoRoot, 'agent-runtime-gateway/21-测试');
const evalFiles = [
  'agent-runtime-gateway/22-评测集/engineering-baseline.json',
  'agent-runtime-gateway/22-评测集/s3-rag-baseline.json',
];
const runnableStages = ['f0', 's0', 's1', 's2', 's3'];
const targets = { tests: 60, evalCases: 100, runnableStages: 12 };

const testFiles = readdirSync(testDir).filter((name) => /^test_.*\.py$/.test(name));
const tests = testFiles.reduce((total, name) => {
  const source = readFileSync(resolve(testDir, name), 'utf8');
  return total + (source.match(/^def test_[a-zA-Z0-9_]+\s*\(/gm) || []).length;
}, 0);

let evalCases = 0;
let assertions = 0;
let explicitAssertions = 0;
for (const path of evalFiles) {
  const payload = JSON.parse(readFileSync(resolve(repoRoot, path), 'utf8'));
  if (!Array.isArray(payload.cases)) throw new Error(`${path} must contain cases[]`);
  evalCases += payload.cases.length;
  explicitAssertions += payload.cases.reduce(
    (total, item) => total + (Array.isArray(item.assertions) ? item.assertions.length : 0),
    0,
  );
  assertions += payload.cases.reduce(
    (total, item) => total
      + (Array.isArray(item.assertions) ? item.assertions.length : 0)
      + (item.expected_status !== undefined ? 1 : 0)
      + (item.expected_refund_executions !== undefined ? 1 : 0),
    0,
  );
}

const metrics = {
  version: '2026-07-15',
  tests,
  evalCases,
  assertions,
  explicitAssertions,
  runnableStages,
  totalStages: targets.runnableStages,
  targets,
  gaps: {
    tests: Math.max(0, targets.tests - tests),
    evalCases: Math.max(0, targets.evalCases - evalCases),
    runnableStages: Math.max(0, targets.runnableStages - runnableStages.length),
  },
  graduationReady:
    tests >= targets.tests
    && evalCases >= targets.evalCases
    && runnableStages.length >= targets.runnableStages,
};

const output = `// Generated by data/build_baseline_metrics.mjs. Do not edit by hand.\nwindow.BASELINE_METRICS = ${JSON.stringify(metrics, null, 2)};\n`;
const outputPath = resolve(dataDir, 'baseline_metrics.js');

if (checkOnly) {
  const current = existsSync(outputPath) ? readFileSync(outputPath, 'utf8') : '';
  if (current !== output) {
    console.error(`Out of date: ${relative(repoRoot, outputPath)}`);
    process.exit(1);
  }
  console.log(
    `OK: ${tests} tests, ${evalCases} eval cases, ${assertions} assertions, ${runnableStages.length}/${targets.runnableStages} runnable stages.`,
  );
} else {
  writeFileSync(outputPath, output, 'utf8');
  console.log(`Generated ${relative(repoRoot, outputPath)}.`);
}
